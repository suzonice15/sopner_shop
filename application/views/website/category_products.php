<script>
    jQuery(document).ready(function () {

        var limit =12;
        var start = 0;
        var action = 'inactive';
        var category_id = jQuery('input[name=category_id]').val();

        function load_data(limit, start) {


            jQuery.ajax({
                url: "<?php echo base_url()?>/ajax/scroll_category_product",
                method: "POST",
                data: {limit: limit, start: start, category_id: category_id},
                cache: false,
                success: function (data) {
                   
                    if (data == '') {
                        jQuery('#load_data_message').html('<h3>No More Result Found</h3>');
                        action = 'active';
                    } else {
                        jQuery('#load_data').append(data);
                        jQuery('#load_data_message').html("");
                        action = 'inactive';
                    }
                }
            })
        }

        if (action == 'inactive') {
            action = 'active';
            load_data(limit, start);
        }

        jQuery(window).scroll(function () {
            if (action == 'inactive') {
                //  alert(related_category)


                action = 'active';
                start = start + limit;
                setTimeout(function () {
                    load_data(limit, start);
                }, 1);
            }
        });

    });
</script>
